from typing import List


class Solution:
    def solveSudoku(self, board: List[List[str]]) -> None:
        """
        Do not return anything, modify board in-place instead.
        """

        def valid_move(r, c, n):
            for i in range(9):
                if board[i][c] == n or board[r][i] == n or board[3 * (r // 3) + (i // 3)][3 * (c // 3) + (i % 3)] == n:
                    return False

            return True

        def backtrack():
            for r in range(9):
                for c in range(9):
                    if board[r][c] == ".":
                        for n in "123456789":
                            if valid_move(r, c, n):
                                board[r][c] = n
                                if backtrack():
                                    return True
                                board[r][c] = "."
                        return False
            return True

        backtrack()


